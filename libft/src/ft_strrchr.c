#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	int len;

	len = ((int)ft_strlen(s) + 1);
	while (--len >= 0)
		if (s[len] == (char)c)
			return ((char *)&s[len]);
	return (NULL);
}
