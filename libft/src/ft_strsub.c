#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*result;
	size_t	i;

	if (!s || start > ft_strlen(s))
		return (NULL);
	if (!(result = ft_strnew(len)))
		return (NULL);
	i = 0;
	while (i < len)
		result[i++] = *(s++ + start);
	return (result);
}
